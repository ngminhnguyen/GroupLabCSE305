
package product;


public class User {
    String userID;
    String password;
    String userName;
    String mail;
    int phoneNumber;

    public User(String userID, String password, String userName, String mail, int phoneNumber) {
        this.userID = userID;
        this.password = password;
        this.userName = userName;
        this.mail = mail;
        this.phoneNumber = phoneNumber;
    }
    
    
}
